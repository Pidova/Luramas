      void XOR(const registrar &registrar, const std::vector<luramas::il::lifter::builder::build::expr> &operands) {

            const auto dest = operands.front();
            const auto src = operands.back();
            const auto result = dest ^ src;

            FOF = 0u;
            FCF = 0u;
            tools::eflags::mutate<xeflags::SF, xeflags::ZF, xeflags::PF>(registrar, tools::eflags::flag_data(registrar.inst, dest, result, src));
            dest = result;
            return;
      }

