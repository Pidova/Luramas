      void WRMSR(const registrar &registrar, const std::vector<luramas::il::lifter::builder::build::expr> &operands) {

            function_handler f(registrar.build);
            const auto temp = klura_vtemp;
            registrar.build->load_internal_global(internal::globals::MSR, temp);

            const auto t = luramas::il::lifter::builder::libraries::structure::concat(f, REG_EDX, REG_EAX);
            temp.write(REG_ECX, REG_ECX + t.bits(), t);
            return;
      }

