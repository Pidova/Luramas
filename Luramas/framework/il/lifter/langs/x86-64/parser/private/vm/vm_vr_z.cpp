      void FXCH(const registrar &registrar, const std::vector<luramas::il::lifter::builder::build::expr> &operands) {

            const auto temp = klura_vtemp;
            if (operands.size() == 1u) {

                  const auto src = operands.front();
                  temp = REG_ST0;
                  REG_ST0 = src;
                  src = temp;
            } else {
                  temp = REG_ST0;
                  REG_ST0 = REG_ST1;
                  REG_ST1 = temp;
            }
            FC1 = 0u;
            return;
      }

